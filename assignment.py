print("num:",num)
num-=10   #num=num-10
print("num:",num)
num*=10   #num=num*10
print("num:",num)
num/=10   #num=num/10
print("num:",num)
num%=10   #num=num%10
print("num:",num)
num**=10   #num=num**10
print("num:",num)
